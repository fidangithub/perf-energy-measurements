def merge_arrays(arr1, arr2):
    def is_ascending(arr):
        return all(arr[i] <= arr[i + 1] for i in range(len(arr) - 1))
    
    ascending1 = is_ascending(arr1)
    ascending2 = is_ascending(arr2)
    
    i, j = 0, 0
    merged = []
    
    while i < len(arr1) and j < len(arr2):
        if (ascending1 and ascending2 and arr1[i] < arr2[j]) or (not ascending1 and not ascending2 and arr1[i] > arr2[j]):
            if not merged or merged[-1] != arr1[i]:
                merged.append(arr1[i])
            i += 1
        elif (ascending1 and ascending2 and arr1[i] > arr2[j]) or (not ascending1 and not ascending2 and arr1[i] < arr2[j]):
            if not merged or merged[-1] != arr2[j]:
                merged.append(arr2[j])
            j += 1
        else:
            if not merged or merged[-1] != arr1[i]:
                merged.append(arr1[i])
            i += 1
            j += 1
    
    while i < len(arr1):
        if not merged or merged[-1] != arr1[i]:
            merged.append(arr1[i])
        i += 1
    
    while j < len(arr2):
        if not merged or merged[-1] != arr2[j]:
            merged.append(arr2[j])
        j += 1
    
    return merged

